// In this program we write two variables: first_number = 12 and second_number = 8.
// We multiply first number with 3 and divide second number by 2.
// We divide the first number by the second.

#include <iostream>
using namespace std;

int main()
{
    int first_number = 12;
    int second_number = 8;
    first_number = 12 * 3;
    second_number = 8 / 2;
    int result = first_number / second_number;
    cout << "The result is: " << result;
    return 0;
}
